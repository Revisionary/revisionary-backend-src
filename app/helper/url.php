<?php
// SITE URLS
function filterUrl($str){
  return htmlspecialchars(trim($str));
}

function get($name){
  if(isset($_GET[$name])){

    if (is_array($_GET[$name])){
      return array_map(function($item){
        return filterUrl($item);
      }, $_GET[$name]);
    }

    return filterUrl($_GET[$name]);

  }
  return false;
}

function post($name){
  if(isset($_POST[$name])){

    if (is_array($_POST[$name])){
      return array_map(function($item){
        return filterUrl($item);
      }, $_POST[$name]);
    }

    return filterUrl($_POST[$name]);

  }
  return false;
}

function url($index){
  global $_url;
  if (isset($_url[$index]))
    return $_url[$index];
  return false;
}

function site_url($url = null, $forceSSL = false, $unForceSSL = false){
  return ($forceSSL ? secure_url : ($unForceSSL ? unsecure_url : url)) . '/' . $url;
}

function asset_url($url = null, $forceSSL = false, $unForceSSL = false){
  return ($forceSSL ? secure_url : ($unForceSSL ? unsecure_url : url)) . '/assets/' . $url;
}

function cache_url($url = null, $forceSSL = false, $unForceSSL = false){
  return asset_url('cache/' . $url, $forceSSL, $unForceSSL);
}

function current_url($add = "", $forceSSL = false, $forceWWW = false) {

	$pageURL = 'http';

	if ( (isset($_SERVER["HTTPS"]) && $_SERVER["HTTPS"] == "on") || $forceSSL )
		$pageURL .= "s";

	$pageURL .= "://";

	if ( $forceWWW && substr($_SERVER["SERVER_NAME"], 0, 4) != "www." )
		$pageURL .= "www.";

	if ($_SERVER["SERVER_PORT"] != "80" && $_SERVER["SERVER_PORT"] != "443")
		$pageURL .= $_SERVER["SERVER_NAME"].":".$_SERVER["SERVER_PORT"].$_SERVER["REQUEST_URI"];
	else
		$pageURL .= $_SERVER["SERVER_NAME"].$_SERVER["REQUEST_URI"];

	if ($add != "") {
		if ( strpos($pageURL,'?') == false ) { $pageURL .= "?".$add; } else { $pageURL .= "&".$add; }
	}

	return $pageURL;
}

